def psmt4_nib(x, y, W):
    """텍스처 (x,y) -> GS 메모리 니블 인덱스 (페이지 행우선, pages_x=W/128)"""
    page = (y // 128) * (W // 128) + (x // 128)
    xx, yy = x % 128, y % 128
    return page * 16384 + blockTable4[yy // 16][xx // 32] * 512 + columnTable4[yy % 16][xx % 32]


def psmct32_word(X, Y, W32):
    """32bpp (X,Y) -> GS 메모리 워드 인덱스"""
    page = (Y // 32) * (W32 // 64) + (X // 64)
    xx, yy = X % 64, Y % 32
    return page * 2048 + blockTable32[yy // 8][xx // 8] * 64 + columnTable32[yy % 8][xx % 8]


def build_file_index_4via32(W, H):
    """'PSMT32 업로드' 4bpp 텍스처: 텍스처 (x,y) -> (파일 니블 인덱스).
    파일은 W32=W/2, H32=H/4 래스터의 32비트 워드 스트림.
    gsmem word w <- file[Y*W32+X] where psmct32_word(X,Y)==w
    """
    W32, H32 = W // 2, H // 4
    # gsmem word -> file word 역매핑
    nwords = W32 * H32
    g2f = [0] * nwords
    for Y in range(H32):
        for X in range(W32):
            g2f[psmct32_word(X, Y, W32)] = Y * W32 + X
    # 텍스처 니블 -> 파일 니블
    idx = [0] * (W * H)
    for y in range(H):
        for x in range(W):
            nib = psmt4_nib(x, y, W)
            fw = g2f[nib >> 3]
            idx[y * W + x] = fw * 8 + (nib & 7)
    return idx


def deswizzle4via32(src, W, H, idx=None):
    if idx is None:
        idx = build_file_index_4via32(W, H)
    out = bytearray(W * H)
    for i, n in enumerate(idx):
        b = src[n >> 1]
        out[i] = (b & 15) if (n & 1) == 0 else (b >> 4)
    return out


def swizzle4via32(pix, W, H, idx=None):
    """역방향: 픽셀(0-15 per px) -> 파일 바이트"""
    if idx is None:
        idx = build_file_index_4via32(W, H)
    out = bytearray(W * H // 2)
    for i, n in enumerate(idx):
        v = pix[i] & 15
        j = n >> 1
        if (n & 1) == 0:
            out[j] = (out[j] & 0xF0) | v
        else:
            out[j] = (out[j] & 0x0F) | (v << 4)
    return out
